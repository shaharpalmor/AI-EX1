package ex1AI;

public class Node {
	int x;
	int y;
	int val;
	
	public Node(int x, int y, int value) {
		this.x = x;
		this.y = y;
		this.val = value;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
	
}
